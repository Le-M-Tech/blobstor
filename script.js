function checkout() {
    if(cart.length === 0) return alert(translations[currentLang]['empty']);
    const detail = cart.map(i => `- ${i.name} (${i.price}€)`).join('%0A');
    window.location.href = `mailto:mbouyssiere@icloud.com?subject=Commande&body=Ma commande :%0A${detail}%0A%0ATotal : ${document.getElementById('total').innerText}€`;
}

function show(id) {
    ['home', 'shop', 'tv', 'reviews', 'contact'].forEach(s => {
        const el = document.getElementById(s);
        if (el) el.classList.add('hidden');
    });
    const target = document.getElementById(id);
    if (target) target.classList.remove('hidden');
    playClick();
}

function playClick() { 
    const sound = document.getElementById('clickSound');
    if(sound) sound.play(); 
}

function toggleDark() { 
    document.body.classList.toggle('dark'); 
    playClick(); 
}

function moveBlobs(e) {
    const x=e.clientX/window.innerWidth; const y=e.clientY/window.innerHeight;
    const b1 = document.getElementById('b1');
    const b2 = document.getElementById('b2');
    if (b1) b1.style.transform=`translate(${x*30}px,${y*30}px)`;
    if (b2) b2.style.transform=`translate(${-x*40}px,${-y*40}px)`;
}
